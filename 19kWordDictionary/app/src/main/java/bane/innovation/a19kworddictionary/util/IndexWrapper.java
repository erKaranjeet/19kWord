package bane.innovation.a19kworddictionary.util;

public class IndexWrapper {

	private int start, end;

	public IndexWrapper(int start, int end) {
		this.start = start;
		this.end = end;
	}

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

}
